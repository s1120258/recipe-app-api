"""
Calculator functions
"""


def add(x, y):
    """
    Add two numbers together.
    :param x: First number
    :param y: Second number
    :return: Sum of x and y
    """
    return x + y


def subtract(x, y):
    """
    Subtract second number from first number.
    :param x: First number
    :param y: Second number
    :return: Result of x - y
    """
    return x - y
